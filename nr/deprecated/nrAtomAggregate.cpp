nrAtomAggregate::nrAtomAggregate( const char* name ) :
    nrAtom( name )
{
}

////////////////////////////////////////////////////////////////////////////

nrAtomAggregate::~nrAtomAggregate( void )
{
    for ( int i = 0; i < m_Atoms.Length(); i++ )
    {
        delete m_Atoms[ i ];
    }
}

