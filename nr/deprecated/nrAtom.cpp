#include "nrAtom.h"

#include <assert.h>
#include <string.h>


////////////////////////////////////////////////////////////////////////////
// Public
////////////////////////////////////////////////////////////////////////////

nrAtom::nrAtom( const char* name )
{
    m_Type = UNKNOWN;

    if ( name != 0 )
    {
        m_Name = new char[ strlen( name ) + 1 ];
        assert( m_Name );    // out of memory.
        strcpy( m_Name, name );
    }
    else
    {
        m_Name = 0;
    }
}

////////////////////////////////////////////////////////////////////////////

nrAtom::~nrAtom( void )
{
    delete [] m_Name;
}

