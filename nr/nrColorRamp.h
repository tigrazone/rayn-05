#ifndef NRCOLORRAMP_H
#define NRCOLORRAMP_H


////////////////////////////////////////////////////////////////////////////
// Includes
////////////////////////////////////////////////////////////////////////////

#include "nrArray.h"
#include "nrColor.h"


////////////////////////////////////////////////////////////////////////////
// Classes
////////////////////////////////////////////////////////////////////////////

class nrImage;

////////////////////////////////////////////////////////////////////////////

class nrColorRamp 
{
public:
	
	nrColorRamp( void );
	~nrColorRamp( void );
	
	// Create a color ramp from a set of colors.
	void CreateFromColors( const nrArray< nrColor >& colors );
	
	// Create a color ramp from an image.  Only the pixels from the first 
	// (top) row of the image are used to compute the color ramp.
	void CreateFromImage( const nrImage& image );
	
	// Return an interpolated color from the ramp.  The input value is
    // clamped to the range [ 0..1 ] where 0 is the beginning of the
    // ramp, and 1 is the end.
	nrColor Color( float value ) const;
	
private:
	
	int      m_NumEntries;
	nrColor* m_ColorRamp;
};

////////////////////////////////////////////////////////////////////////////

#endif  // NRCOLORRAMP_H
